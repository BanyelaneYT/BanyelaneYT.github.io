				<h2><strong>G&eacute;neros literarios: tipos</strong></h2>

<p>La literatura y sus obras se agrupan en lo que se conoce como g&eacute;neros literarios, que act&uacute;an como clasificadores en funci&oacute;n de las caracter&iacute;sticas y los rasgos comunes entre los diferentes escritos. Esta clasificaci&oacute;n se lleva a cabo atendiendo a la estructura, el tema o el fin que persigue el autor. Existen tres tipos de estilos literarios principales:<strong> la narrativa, la l&iacute;rica y la dram&aacute;tica</strong><br/>.</p>

<h3><strong>Narrativa</strong></h3>

<p><br/>La narrativa tambi&eacute;n se conoce como <strong>g&eacute;nero &eacute;pico</strong> y consiste en que el autor haga uso de la figura del narrador para contar los hechos protagonizados por unos personajes. El escritor presenta una <strong>realidad ficticia</strong> o<strong> realidad representada </strong>como si fuese el mundo exterior u objetivo, ajeno al autor.<br/></p>

<p><br/>Este g&eacute;nero se caracteriza por la abundancia de descripciones, tanto de personas, como de situaciones y ambientes. Tambi&eacute;n relata acciones y acontecimientos en un espacio y tiempo determinado e incluye mon&oacute;logos y di&aacute;logos entre los personajes. Lo importante de este g&eacute;nero es el narrador, el encargado de contar la historia a trav&eacute;s de la narraci&oacute;n, la descripci&oacute;n, la exposici&oacute;n o la argumentaci&oacute;n. Puede ser de cuatro tipos:<br/></p>

<ul>
	<li>
	<p><strong><br/>Narrador omnisciente: </strong>lo sabe todo y describe la escena desde el exterior, por lo que el autor emplea la tercera persona.</p>
	</li>
	<li>
	<p><strong><br/>Narrador testigo u observador: </strong>narra aquellas partes de la historia que &eacute;l ha presenciado o ha conocido. No es como el primero que lo sabe todo y lo cuenta.</p>
	</li>
	<li>
	<p><strong><br/>Narrador en primera persona:</strong> Se usa la primera persona porque el narrador es el protagonista. En este caso, el texto adoptar&iacute;a una forma autobiogr&aacute;fica.</p>
	</li>
	<li>
	<p><strong><br/>Narraciones en forma epistolar: </strong>Esta forma de narrar la historia es muy especial, ya que la historia se cuenta a trav&eacute;s de cartas entre dos personajes.</p>

<p><br/>El subg&eacute;nero por antonomasia de este estilo literario es la<strong> novela</strong>, una extensa y compleja narraci&oacute;n en la que predomina la narraci&oacute;n y la descripci&oacute;n y el di&aacute;logo. En la novela se presentan diferentes personajes sumergidos en diferentes tramas que tienen lugar en espacios, &eacute;pocas y tiempos muy diversos.</p>

<p><br/>Dentro de la novela se encuentra el cuento, donde se desarrolla un argumento breve. Asimismo, acoge otros subg&eacute;neros como<strong> la bizantina, la novela caballeresca y cortesana, el libro de caballer&iacute;as, la novela sentimental, pastoril, morisca, picaresca, pedag&oacute;gica o pol&iacute;tica.</strong></p>

<p>Dentro de los g&eacute;neros narrativos menores se encuentra<strong> la leyenda</strong>, un cuento o poema breve de asunto tradicional o vagamente hist&oacute;rico donde conviven elementos fant&aacute;sticos y sobrenaturales con un ambiente misterioso y que se suele decir que est&aacute; basado en hechos reales; y<strong> la f&aacute;bula,</strong> un cuento did&aacute;ctico y moralizante que emplea animales con cualidades humanas para desprender una moral que se explica al final bajo el nombre de moraleja.</p>

<p><strong><br/>La narrativa</strong> tambi&eacute;n puede acoger textos en verso, en cuyo caso estar&iacute;amos hablando de <strong>epopeyas</strong>, poemas extensos donde se narran hechos importantes de la historia de un pueblo como en <strong>La Il&iacute;ada</strong> y<strong> La Odisea de Homero</strong>; de cantares de gesta, poemas largos donde se narran las haza&ntilde;as de un h&eacute;roe como <strong>El cantar del M&iacute;o Cid</strong> de autor desconocido; o de romances, poemas &eacute;pico-l&iacute;ricos usados para narrar haza&ntilde;as o hechos b&eacute;licos y cuyo origen es an&oacute;nimo-popular.</p>

	<h3><strong><br/>L&iacute;rica</strong></h3>

	<p><br/>La l&iacute;rica o g&eacute;nero l&iacute;rico es aquel que agrupa los textos en los que el autor expresas sus emociones o sentimientos en verso o en prosa. Es una tipolog&iacute;a muy subjetiva ya que el autor presenta la realidad desde su propia perspectiva y utiliza el arte de la palabra para expresar sus pensamientos, reflejar sus estados de &aacute;nimo, sus vivencias o sentimientos. Por tanto, predomina el uso de la primera persona del singular.</p>

	<p><br/>Dentro de la l&iacute;rica se encuentran otros subg&eacute;neros:</p>
	</li>
	<li>
	<p><strong><br/>La canci&oacute;n:</strong> Suele tener un tema amoroso como hilo conductor, aunque tambi&eacute;n acoge otros temas como la naturaleza, la belleza, la amistad o la religi&oacute;n. Asimismo, se configura como un poema admirativo y emocionado.</p>
	</li>
	<li>
	<p><strong><br/>La oda:</strong> Es un poema l&iacute;rico cuya finalidad es la exaltaci&oacute;n de una persona o cosa. Trata un tema serio y emplea un tono elevado y pasional.</p>
	</li>
	<li>
	<p><strong><br/>La eleg&iacute;a:</strong> Es la expresi&oacute;n del dolor que causa la muerte, la p&eacute;rdida, la separaci&oacute;n o la ausencia de un ser querido. Tambi&eacute;n hace referencia a desgracias colectivas y opera como un poema meditativo, melanc&oacute;lico y moralizante.</p>
	</li>
	<li>
	<p><strong><br/>El himno:</strong> Es una canci&oacute;n que adopta un tono m&aacute;s exaltado y puede ser religiosa, patri&oacute;tica o una celebraci&oacute;n a la uni&oacute;n y la amistad.</p>
	</li>
	<li>
	<p><strong><br/>La s&aacute;tira: </strong>El poema se configura como una cr&iacute;tica burlesca y ridiculizadora sobre temas censurables, tanto del plano individual como colectivo.</p>
	</li>
	<li>
	<p><strong><br/>La &eacute;gloga:</strong> En los poemas de este tipo se lleva a cabo una exposici&oacute;n de los sentimientos amorosos y de exaltaci&oacute;n de la naturaleza puesta en boca de pastores, por lo que suele adoptar forma de di&aacute;logo.</p>
	</li>
	<li>
	<p><strong><br/>El epigrama:</strong> Es una s&aacute;tira de menor extensi&oacute;n.</p>
	</li>
	<li>
	<p><strong><br/>La letrilla:</strong> Es un poema estr&oacute;fico con estribillo de metro corto pensado para ser cantado.</p>
	</li>
	<li>
	<p><strong><br/>El madrigal:</strong> Forma parte de los g&eacute;neros menores de la l&iacute;rica y se define como un poema amoroso, dulce y amable. En s&iacute;ntesis, un piropo hecho verso.</p>

	<h3><strong><br/>Dram&aacute;tica</strong></h3>

	<p><br/>El g&eacute;nero dram&aacute;tico tambi&eacute;n es conocido como g&eacute;nero teatral y acoge obras en forma de di&aacute;logo con la intenci&oacute;n de que sean representadas en un escenario. Las obras dram&aacute;ticas siguen su curso a partir de las interrelaciones de los personajes que, sin que el autor los represente o describa y sin especificar qu&eacute; hacen o sienten, el p&uacute;blico los va conociendo a partir del di&aacute;logo que se crea entre ellos.</p>

	<p><br/>Dentro de este g&eacute;nero se encuentran los siguientes subg&eacute;neros:</p>
	</li>
	<li>

	<p><strong><br/>La tragedia:</strong> una obra dram&aacute;tica con un final desgraciado. Suele contar los conflictos que ocurren entre personajes de alta alcurnia, v&iacute;ctimas de pasiones que les llevan a la destrucci&oacute;n y a la muerte. Un ejemplo cl&aacute;sico es Romeo y Julieta de William Shakespeare.</p>
	</li>
	<li>

	<p><strong><br/>La comedia:</strong> una obra caracterizada por un final feliz y por el uso de un tono humor&iacute;stico. En este caso se desarrollan situaciones divertidas y amables que atienden al enredo y a la intriga con personajes m&aacute;s alejados de la clase alta. La intenci&oacute;n es provocar la risa del espectador.</p>
	</li>
	<li>

	<p><strong><br/>El drama o tragicomedia:</strong> Es el subg&eacute;nero intermedio entre la tragedia y la comedia, ya que las pasiones no alcanzan el nivel de intensidad de la tragedia y, en ocasiones, se intercalan elementos c&oacute;micos. En este caso, los personajes protagonistas luchan contra la adversidad y muestran un gran dolor y el final puede llegar a ser desastroso.</p>
	</li>
</ul>
